const SearchInput = document.querySelector("#search-item");
const allProduct = document.querySelector(".product-list");

function Output(Product) {
  let html = "";
  Product.forEach((Products) => {
    html += `
    <div class="col-lg-4 col-md-6 col-12 box">
    <div class="card mb-3">
        <img class "card-img-top rounded-3 food-img" src="${Products.img}" alt="Card image cap ">
        <div class = "card-body mb-3">
        <h5 class = "card-title food-title'">${Products.name}</h5>
        <p class = "card-text">${Products.description}</p>
        <p class = "card-text price">Price : $${Products.price}</p>
        <button class="btn btn-primary add">Add to Card</button>
        </div>
    </div>
</div>
    `;
  });
  return html;
}
function updateProductlist(Product) {
  allProduct.innerHTML = Output(Product);
}

